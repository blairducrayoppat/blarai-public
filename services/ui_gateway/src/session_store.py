@dataclass(frozen=True)
class SessionSummary:
    """Lightweight session descriptor for the session list panel."""

    id: str
    """UUID of the session."""

    title: str
    """Auto-generated title from the first prompt."""

    created_at: str
    """ISO 8601 timestamp of session creation."""

    updated_at: str
    """ISO 8601 timestamp of last activity."""

    is_active: bool
    """True if this is the currently active session."""

    turn_count: int = 0
    """Number of turns in this session."""


@dataclass(frozen=True)
class Turn:
    """Single conversation turn (user or assistant)."""

    id: str
    """UUID of the turn."""

    session_id: str
    """FK to the parent session."""

    role: str
    """'user' or 'assistant'."""

    content: str
    """Text content of the turn."""

    pgov_status: str
    """'approved', 'denied', 'error', 'N/A' (user turns), or 'informational'
    (deterministic gateway/tool output — see INFORMATIONAL_TURN_STATUS)."""

    pgov_reasons: list[str] = field(default_factory=list)
    """PGOV reason codes (empty for approved or user turns)."""

    timestamp: str = ""
    """ISO 8601 timestamp of the turn."""


