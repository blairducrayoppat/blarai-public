"""
SQLite Session Persistence for the UI Transport Gateway (P1.11).

Stores conversation sessions and turns in a local SQLite database at
%LOCALAPPDATA%\\BlarAI\\sessions.db. No cloud sync. No external network calls.

Schema:
    sessions: id (UUID PK), title, created_at, updated_at, is_active
    turns: id (UUID PK), session_id (FK), role, content, pgov_status, pgov_reasons, timestamp

CASCADE delete: deleting a session removes all its turns.

At-rest encryption (Sprint 14, ADR-025):
    ``turns.content`` and ``sessions.title`` (the content-bearing columns) are
    AES-256-GCM encrypted on write and decrypted on read in
    :class:`EncryptedSessionStore`.  The DEK is dual-wrapped (TPM + offline
    recovery key) and shared with the substrate store via the same
    ``BLARAI_DEK_KEYSTORE`` env var and ``TpmSealer("BlarAI-DEKSeal")`` key name
    (ADR-025 section 2.1 -- one DEK, one envelope).

    AAD binding (ADR-025 section 2.4): both ``turns.id`` and ``sessions.id`` are
    UUID strings generated by the application **before insert**
    (``str(uuid.uuid4())``), so they are stable row identities known at insert
    time.  AAD = ``make_aad_for(table, column, row_uuid)``.

    ``session_id`` stays plaintext -- it is a relational/join key, not content
    (SDV section 5.3; encrypting it would break joins for zero confidentiality
    gain).  Low-value metadata (``role``, ``pgov_status``, ``pgov_reasons``,
    timestamps, ``is_active``) stays plaintext.

    Fail-closed: if the DEK cannot be unsealed, the store refuses to open
    (``DekEnvelopeError`` propagates out of the constructor).  There is NO
    plaintext fallback, ever.
"""

