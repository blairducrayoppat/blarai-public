_SCHEMA_SQL: str = """
CREATE TABLE IF NOT EXISTS sessions (
    id         TEXT PRIMARY KEY,
    title      TEXT NOT NULL DEFAULT '',
    created_at TEXT NOT NULL,
    updated_at TEXT NOT NULL,
    is_active  INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS turns (
    id           TEXT PRIMARY KEY,
    session_id   TEXT NOT NULL,
    role         TEXT NOT NULL CHECK(role IN ('user', 'assistant')),
    content      TEXT NOT NULL DEFAULT '',
    pgov_status  TEXT NOT NULL DEFAULT 'N/A',
    pgov_reasons TEXT NOT NULL DEFAULT '[]',
    timestamp    TEXT NOT NULL,
    FOREIGN KEY (session_id) REFERENCES sessions(id) ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_turns_session ON turns(session_id);
"""


class SessionStore:
    """SQLite-backed session persistence.

    Usage:
        store = SessionStore(db_path=":memory:")  # or real path
        sid = store.create_session(title="My Chat")
        store.add_turn(sid, "user", "Hello", "N/A", [])
        sessions = store.list_sessions()
    """

    def __init__(self, db_path: str = ":memory:") -> None:
        """Initialize the session store.

        Args:
            db_path: Path to SQLite database file. Use ':memory:' for tests.
                For production, use SESSION_DB_PATH from constants.py.
        """
        self._db_path: str = db_path

        # Ensure directory exists for file-based DBs
        if db_path != ":memory:":
            db_dir = os.path.dirname(db_path)
            if db_dir:
                os.makedirs(db_dir, exist_ok=True)

        self._conn: sqlite3.Connection = sqlite3.connect(
            db_path,
            check_same_thread=False,
        )
        _harden_db_file_dacl(db_path)
        self._conn.execute("PRAGMA journal_mode=WAL")
        self._conn.execute("PRAGMA foreign_keys=ON")
        # secure_delete=ON (FULL): DELETEd rows are zeroed in freed pages instead
        # of just being marked free. This store is in WAL mode, so freed pages are
        # zeroed at checkpoint (rollback-journal stores zero at COMMIT).
        self._conn.execute("PRAGMA secure_delete=ON")
        self._conn.executescript(_SCHEMA_SQL)
        self._conn.commit()
        # One-time data repair: give pre-existing empty-title sessions a
        # derived title. Idempotent -- a no-op once the DB has been repaired.
        self._backfill_empty_titles()
        logger.info("SessionStore initialized: %s", db_path)

    def close(self) -> None:
        """Close the database connection."""
        self._conn.close()
        logger.info("SessionStore closed")

    def create_session(self, title: str = "") -> str:
        """Create a new session.

        Args:
            title: Session title (auto-truncated to SESSION_TITLE_MAX_CHARS).

        Returns:
            UUID string of the new session.
        """
        session_id = str(uuid.uuid4())
        now = datetime.now(timezone.utc).isoformat()
        truncated_title = title[:SESSION_TITLE_MAX_CHARS] if title else ""

        self._conn.execute(
            "INSERT INTO sessions (id, title, created_at, updated_at, is_active) "
            "VALUES (?, ?, ?, ?, 1)",
            (session_id, truncated_title, now, now),
        )
        self._conn.commit()
        logger.info("Session created: %s (%s)", session_id, truncated_title)
        return session_id

    def list_sessions(self) -> list[SessionSummary]:
        """List all sessions ordered by most recently updated.

        Returns:
            List of SessionSummary objects.
        """
        cursor = self._conn.execute(
            "SELECT s.id, s.title, s.created_at, s.updated_at, s.is_active, "
            "  (SELECT COUNT(*) FROM turns t WHERE t.session_id = s.id) AS turn_count "
            "FROM sessions s "
            "ORDER BY s.updated_at DESC"
        )
        return [
            SessionSummary(
                id=row[0],
                title=row[1],
                created_at=row[2],
                updated_at=row[3],
                is_active=bool(row[4]),
                turn_count=row[5],
            )
            for row in cursor.fetchall()
        ]

    def get_session_turns(self, session_id: str) -> list[Turn]:
        """Retrieve all turns for a session, ordered chronologically.

        Args:
            session_id: UUID of the session.

        Returns:
            List of Turn objects.
        """
        cursor = self._conn.execute(
            "SELECT id, session_id, role, content, pgov_status, pgov_reasons, timestamp "
            "FROM turns "
            "WHERE session_id = ? "
            "ORDER BY timestamp ASC",
            (session_id,),
        )
        return [
            Turn(
                id=row[0],
                session_id=row[1],
                role=row[2],
                content=row[3],
                pgov_status=row[4],
                pgov_reasons=json.loads(row[5]) if row[5] else [],
                timestamp=row[6],
            )
            for row in cursor.fetchall()
        ]

    def get_turns(self, session_id: str) -> list[Turn]:
        """Alias for get_session_turns()."""
        return self.get_session_turns(session_id)

    def add_turn(
        self,
        session_id: str,
        role: str,
        content: str,
        pgov_status: str,
        pgov_reasons: list[str],
    ) -> str:
        """Add a turn to a session.

        Args:
            session_id: UUID of the parent session.
            role: 'user' or 'assistant'.
            content: Text content.
            pgov_status: 'approved', 'denied', 'error', or 'N/A'.
            pgov_reasons: List of PGOV reason code strings.

        Returns:
            UUID of the new turn.

        Raises:
            ValueError: If role is not 'user' or 'assistant'.
        """
        if role not in ("user", "assistant"):
            raise ValueError(f"Invalid role: {role!r}. Must be 'user' or 'assistant'.")

        turn_id = str(uuid.uuid4())
        now = datetime.now(timezone.utc).isoformat()

        self._conn.execute(
            "INSERT INTO turns (id, session_id, role, content, pgov_status, pgov_reasons, timestamp) "
            "VALUES (?, ?, ?, ?, ?, ?, ?)",
            (turn_id, session_id, role, content, pgov_status, json.dumps(pgov_reasons), now),
        )
        # Update session's updated_at
        self._conn.execute(
            "UPDATE sessions SET updated_at = ? WHERE id = ?",
            (now, session_id),
        )
        self._conn.commit()
        return turn_id

    def delete_session(self, session_id: str) -> bool:
        """Delete a session and all its turns (CASCADE).

        Args:
            session_id: UUID of the session to delete.

        Returns:
            True if the session existed and was deleted.
        """
        cursor = self._conn.execute(
            "DELETE FROM sessions WHERE id = ?",
            (session_id,),
        )
        self._conn.commit()
        deleted = cursor.rowcount > 0
        if deleted:
            logger.info("Session deleted: %s (CASCADE turns)", session_id)
        return deleted

    def clear_session_turns(self, session_id: str) -> int:
        """Delete all turns for a session, preserving the session record.

        Args:
            session_id: UUID of the session.

        Returns:
            Number of turns deleted.
        """
        cursor = self._conn.execute(
            "DELETE FROM turns WHERE session_id = ?",
            (session_id,),
        )
        self._conn.commit()
        logger.info("Cleared %d turns for session %s", cursor.rowcount, session_id)
        return cursor.rowcount

    def set_active_session(self, session_id: str) -> None:
        """Mark a session as active and deactivate all others.

        Args:
            session_id: UUID of the session to activate.
        """
        self._conn.execute("UPDATE sessions SET is_active = 0")
        self._conn.execute(
            "UPDATE sessions SET is_active = 1 WHERE id = ?",
            (session_id,),
        )
        self._conn.commit()

    # -- Title management --------------------------------------------------

    def set_title_if_empty(self, session_id: str, title: str) -> bool:
        """Set a session's title only if it is currently empty.

        Applies the auto-generated title on a session's first user prompt.
        The ``WHERE title = ''`` guard makes this idempotent and ensures it
        can never clobber a title the user set explicitly via /rename.

        Args:
            session_id: UUID of the session.
            title: Title to set (truncated to ``SESSION_TITLE_MAX_CHARS``).

        Returns:
            True if a row was updated (title was empty and is now set).
        """
        truncated = title[:SESSION_TITLE_MAX_CHARS]
        cursor = self._conn.execute(
            "UPDATE sessions SET title = ? WHERE id = ? AND title = ''",
            (truncated, session_id),
        )
        self._conn.commit()
        updated = cursor.rowcount > 0
        if updated:
            logger.info("Session title set: %s (%s)", session_id, truncated)
        return updated

    def update_session_title(self, session_id: str, title: str) -> bool:
        """Set a session's title unconditionally -- backs the /rename command.

        Unlike :meth:`set_title_if_empty`, this overwrites any existing
        title; that is the intent of an explicit user rename.

        Args:
            session_id: UUID of the session.
            title: New title (truncated to ``SESSION_TITLE_MAX_CHARS``).

        Returns:
            True if the session existed and was updated.
        """
        truncated = title[:SESSION_TITLE_MAX_CHARS]
        cursor = self._conn.execute(
            "UPDATE sessions SET title = ? WHERE id = ?",
            (truncated, session_id),
        )
        self._conn.commit()
        updated = cursor.rowcount > 0
        if updated:
            logger.info("Session renamed: %s (%s)", session_id, truncated)
        return updated

    def _backfill_empty_titles(self) -> int:
        """One-time data repair: give empty-title sessions a derived title.

        Historically the auto-title feature was unimplemented, so every
        session row was stored with an empty title and surfaced in the UI
        as the indistinguishable placeholder "New session". This pass
        repairs that bad data: for each session whose title is still empty,
        it derives a title from the session's first user turn (the same
        rule new sessions now follow) and writes it back.

        Purely additive and idempotent:
          - Only sessions with an empty title are touched; existing titles
            (including /rename titles) are never modified.
          - A session with no user turn is left empty -- genuinely unused,
            with no content to name it after.
          - After one run, repaired sessions no longer match the
            empty-title filter, so subsequent runs are no-ops.

        Returns:
            Number of sessions backfilled.
        """
        rows = self._conn.execute(
            "SELECT id, created_at FROM sessions WHERE title = ''"
        ).fetchall()
        backfilled = 0
        for session_id, created_at in rows:
            turn = self._conn.execute(
                "SELECT content, timestamp FROM turns "
                "WHERE session_id = ? AND role = 'user' "
                "ORDER BY timestamp ASC LIMIT 1",
                (session_id,),
            ).fetchone()
            if turn is None:
                continue  # no user turn -- nothing to name the session after
            content, turn_ts = turn
            when = _parse_local_datetime(created_at or turn_ts)
            title = derive_session_title(content, when)
            self._conn.execute(
                "UPDATE sessions SET title = ? WHERE id = ?",
                (title, session_id),
            )
            backfilled += 1
        if backfilled:
            self._conn.commit()
            logger.info("Backfilled %d empty session title(s)", backfilled)
        return backfilled


