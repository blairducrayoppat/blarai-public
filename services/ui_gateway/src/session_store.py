# Version marker stored in ``sessions_meta`` to identify encrypted stores.
_SESSIONS_ENCRYPTION_VERSION: str = "1"

# SQL for the sessions_meta table -- a simple key/value store for schema metadata.
_META_TABLE_SQL: str = """
CREATE TABLE IF NOT EXISTS sessions_meta (
    key   TEXT PRIMARY KEY,
    value TEXT NOT NULL
);
"""


def _is_encrypted_blob(value: Any) -> bool:
    """Return True if *value* looks like an AES-GCM ciphertext blob.

    An encrypted field is stored as a ``BLOB`` whose first byte is the
    ``FIELD_CIPHER_VERSION`` constant (0x01).  Plaintext fields are ``TEXT``
    (Python ``str``).  This check lets the migration and backfill paths detect
    already-encrypted rows without a dedicated schema column.
    """
    from shared.security.field_cipher import FIELD_CIPHER_VERSION
    return (
        isinstance(value, (bytes, bytearray))
        and len(value) > 0
        and value[0] == FIELD_CIPHER_VERSION
    )


class EncryptedSessionStore:
    """AES-GCM-encrypted variant of SessionStore (Sprint 14, ADR-025).

    Sensitive columns encrypted on write / decrypted on read:
    - ``turns.content`` -- every conversation turn's text.
    - ``sessions.title`` -- the prompt-derived session title.

    ``session_id`` and all other metadata (``role``, ``pgov_status``,
    ``pgov_reasons``, timestamps, ``is_active``) stay plaintext -- they are
    relational/metadata fields, not content (ADR-025 section 3, SDV section 5.3).

    AAD binding (ADR-025 section 2.4): both ``turns.id`` and ``sessions.id``
    are UUID strings generated by the application **before insert** -- stable
    row identities known at insert time.
    AAD = ``make_aad_for(table, column, row_uuid)``.

    Fail-closed: the constructor raises ``DekEnvelopeError`` if the DEK cannot
    be unsealed.  There is NO plaintext fallback.

    ``has_encryption: bool = True`` -- production-wiring regression lock.

    Args:
        db_path: Path to the sessions SQLite file (``:memory:`` for tests).
        cipher:  :class:`~shared.security.field_cipher.FieldCipher` instance,
                 constructed by the caller after unsealing the DEK envelope.
    """

    #: Regression-lock attribute: any code that constructs the store can assert
    #: ``store.has_encryption is True`` to detect a future silent-wiring failure.
    has_encryption: bool = True

    def __init__(
        self,
        db_path: str,
        cipher: "FieldCipher",  # type: ignore[name-defined]  # noqa: F821
    ) -> None:
        from shared.security.field_cipher import FieldCipher  # local import; no circular dep
        if not isinstance(cipher, FieldCipher):
            raise TypeError(
                "EncryptedSessionStore requires a FieldCipher instance; "
                f"got {type(cipher).__name__!r}.  Pass a FieldCipher derived "
                "from the unsealed DEK."
            )
        self._db_path: str = db_path
        self._cipher = cipher

        # Ensure directory exists for file-based DBs
        if db_path != ":memory:":
            db_dir = os.path.dirname(db_path)
            if db_dir:
                os.makedirs(db_dir, exist_ok=True)

        self._conn: sqlite3.Connection = sqlite3.connect(
            db_path,
            check_same_thread=False,
        )
        _harden_db_file_dacl(db_path)
        self._conn.execute("PRAGMA journal_mode=WAL")
        self._conn.execute("PRAGMA foreign_keys=ON")
        # secure_delete=ON (FULL): DELETEd rows are zeroed in freed pages instead
        # of just being marked free. This store is in WAL mode, so freed pages are
        # zeroed at checkpoint (rollback-journal stores zero at COMMIT).
        self._conn.execute("PRAGMA secure_delete=ON")
        self._conn.executescript(_SCHEMA_SQL)
        self._conn.executescript(_META_TABLE_SQL)
        # Mark this store as encryption-version 1.
        self._conn.execute(
            "INSERT OR IGNORE INTO sessions_meta(key, value) VALUES('encryption_version', ?)",
            (_SESSIONS_ENCRYPTION_VERSION,),
        )
        self._conn.commit()
        # Backfill: sessions with empty titles get derived titles (via decrypted
        # content).  Idempotent -- a no-op once previously-empty titles have been
        # set.
        self._backfill_empty_titles()
        logger.info("EncryptedSessionStore initialized: %s", db_path)

    def close(self) -> None:
        """Close the database connection."""
        self._conn.close()
        logger.info("EncryptedSessionStore closed")

    @property
    def field_cipher(self) -> "FieldCipher":  # type: ignore[name-defined]  # noqa: F821
        """The store's :class:`FieldCipher` (the shared-DEK envelope cipher).

        Exposed read-only so sibling shared-DEK consumers in the SAME process
        -- specifically the #655 ingest-staging writer in the gateway
        (``shared/security/ingest_staging.write_staged``) -- can encrypt under
        the one-DEK envelope (ADR-025 section 2.1) without duplicating the
        sealer/keystore resolution policy in :func:`build_session_store`.

        This adds no at-rest or cross-process surface: the derived subkeys
        already live in this process's memory; the property only spares a
        second envelope-unseal of the same DEK.
        """
        return self._cipher

    # -- Encryption helpers ------------------------------------------------

    def _enc_turn_content(self, turn_id: str, plaintext: str) -> bytes:
        """Encrypt ``turns.content`` for *turn_id*."""
        from shared.security.field_cipher import make_aad_for
        return self._cipher.encrypt(
            plaintext.encode("utf-8"),
            aad=make_aad_for("turns", "content", turn_id),
        )

    def _dec_turn_content(self, turn_id: str, blob: Any) -> str:
        """Decrypt ``turns.content`` for *turn_id*.  Fail-closed."""
        from shared.security.field_cipher import FieldCipherError, make_aad_for
        if not _is_encrypted_blob(blob):
            # Plaintext row -- pre-migration or in-memory store without migration.
            return blob if isinstance(blob, str) else bytes(blob).decode("utf-8")
        try:
            return self._cipher.decrypt(
                bytes(blob),
                aad=make_aad_for("turns", "content", turn_id),
            ).decode("utf-8")
        except FieldCipherError as exc:
            raise RuntimeError(
                f"EncryptedSessionStore: failed to decrypt turns.content for "
                f"turn_id={turn_id!r} -- refusing to return plaintext"
            ) from exc

    def _enc_session_title(self, session_id: str, plaintext: str) -> bytes:
        """Encrypt ``sessions.title`` for *session_id*."""
        from shared.security.field_cipher import make_aad_for
        return self._cipher.encrypt(
            plaintext.encode("utf-8"),
            aad=make_aad_for("sessions", "title", session_id),
        )

    def _dec_session_title(self, session_id: str, blob: Any) -> str:
        """Decrypt ``sessions.title`` for *session_id*.  Fail-closed.

        Empty title (stored as b'' or '') is returned as '' without decryption --
        an empty title is valid state (newly-created session before backfill).
        """
        from shared.security.field_cipher import FieldCipherError, make_aad_for
        if blob is None or blob == b"" or blob == "":
            return ""
        if not _is_encrypted_blob(blob):
            # Plaintext row -- pre-migration or in-memory store without migration.
            return blob if isinstance(blob, str) else bytes(blob).decode("utf-8")
        try:
            return self._cipher.decrypt(
                bytes(blob),
                aad=make_aad_for("sessions", "title", session_id),
            ).decode("utf-8")
        except FieldCipherError as exc:
            raise RuntimeError(
                f"EncryptedSessionStore: failed to decrypt sessions.title for "
                f"session_id={session_id!r} -- refusing to return plaintext"
            ) from exc

    # -- Session CRUD -------------------------------------------------------

    def create_session(self, title: str = "") -> str:
        """Create a new session with the title AES-GCM encrypted.

        Args:
            title: Session title (truncated to ``SESSION_TITLE_MAX_CHARS``).

        Returns:
            UUID string of the new session.
        """
        session_id = str(uuid.uuid4())
        now = datetime.now(timezone.utc).isoformat()
        truncated_title = title[:SESSION_TITLE_MAX_CHARS] if title else ""

        # Encrypt the title; empty title is stored as '' (not as a cipher blob).
        if truncated_title:
            enc_title: Any = self._enc_session_title(session_id, truncated_title)
        else:
            enc_title = ""

        self._conn.execute(
            "INSERT INTO sessions (id, title, created_at, updated_at, is_active) "
            "VALUES (?, ?, ?, ?, 1)",
            (session_id, enc_title, now, now),
        )
        self._conn.commit()
        logger.info("EncryptedSessionStore: session created: %s", session_id)
        return session_id

    def list_sessions(self) -> list[SessionSummary]:
        """List all sessions ordered by most recently updated.

        Decrypts ``sessions.title`` for each row on read.

        Bulk-read quarantine policy (ADR-025 §2.7 amendment, 2026-06-06):
        A single legacy or tampered row must not deny access to the entire
        store -- that would be a self-inflicted availability DoS, and
        availability is a security property (CIA triad).  When a row cannot
        be decrypted (e.g. encrypted under a prior dev-mode key after a
        dev->production key transition) it is QUARANTINED: omitted from the
        result set and logged at WARNING level with a stable event code
        SESSION_ROW_DECRYPT_QUARANTINE.  Confidentiality and integrity are
        fully preserved -- plaintext is never returned, tampered data is
        never trusted -- and the quarantine event makes a bad row MORE
        visible than a generic store-wide crash.

        SINGLE-RECORD callers (set_title_if_empty, update_session_title)
        retain the hard fail-closed posture: the caller named that one
        session explicitly, so a decrypt failure is the right loud signal.

        Returns:
            List of SessionSummary objects with plaintext titles.
            Rows that cannot be decrypted are omitted (quarantined).
        """
        cursor = self._conn.execute(
            "SELECT s.id, s.title, s.created_at, s.updated_at, s.is_active, "
            "  (SELECT COUNT(*) FROM turns t WHERE t.session_id = s.id) AS turn_count "
            "FROM sessions s "
            "ORDER BY s.updated_at DESC"
        )
        summaries: list[SessionSummary] = []
        quarantined: int = 0
        for row in cursor.fetchall():
            session_id, raw_title, created_at, updated_at, is_active, turn_count = row
            try:
                title = self._dec_session_title(session_id, raw_title)
            except RuntimeError as exc:
                quarantined += 1
                logger.warning(
                    "SESSION_ROW_DECRYPT_QUARANTINE event=SESSION_ROW_DECRYPT_QUARANTINE "
                    "session_id=%r reason=%r -- row excluded from list_sessions result",
                    session_id,
                    str(exc),
                )
                continue
            summaries.append(SessionSummary(
                id=session_id,
                title=title,
                created_at=created_at,
                updated_at=updated_at,
                is_active=bool(is_active),
                turn_count=turn_count,
            ))
        if quarantined:
            logger.warning(
                "SESSION_ROW_DECRYPT_QUARANTINE summary: %d session row(s) quarantined "
                "in list_sessions -- check key rotation / dev->prod key transition",
                quarantined,
            )
        return summaries

    def get_session_turns(self, session_id: str) -> list[Turn]:
        """Retrieve all turns for a session, decrypting ``content`` on read.

        Bulk-read quarantine policy (ADR-025 §2.7 amendment, 2026-06-06):
        Same rationale as list_sessions -- a single un-decryptable turn
        (legacy key, tampered ciphertext) must not deny access to all other
        turns in the session.  Un-decryptable turns are quarantined: omitted
        from the result and logged with SESSION_ROW_DECRYPT_QUARANTINE.
        Plaintext is never returned from a bad row.

        Args:
            session_id: UUID of the session.

        Returns:
            List of Turn objects with plaintext content.
            Turns that cannot be decrypted are omitted (quarantined).
        """
        cursor = self._conn.execute(
            "SELECT id, session_id, role, content, pgov_status, pgov_reasons, timestamp "
            "FROM turns "
            "WHERE session_id = ? "
            "ORDER BY timestamp ASC",
            (session_id,),
        )
        turns: list[Turn] = []
        quarantined: int = 0
        for row in cursor.fetchall():
            turn_id, sess_id, role, raw_content, pgov_status, pgov_reasons_raw, ts = row
            try:
                content = self._dec_turn_content(turn_id, raw_content)
            except RuntimeError as exc:
                quarantined += 1
                logger.warning(
                    "SESSION_ROW_DECRYPT_QUARANTINE event=SESSION_ROW_DECRYPT_QUARANTINE "
                    "turn_id=%r session_id=%r reason=%r -- turn excluded from "
                    "get_session_turns result",
                    turn_id,
                    session_id,
                    str(exc),
                )
                continue
            turns.append(Turn(
                id=turn_id,
                session_id=sess_id,
                role=role,
                content=content,
                pgov_status=pgov_status,
                pgov_reasons=json.loads(pgov_reasons_raw) if pgov_reasons_raw else [],
                timestamp=ts,
            ))
        if quarantined:
            logger.warning(
                "SESSION_ROW_DECRYPT_QUARANTINE summary: %d turn row(s) quarantined "
                "in get_session_turns(session_id=%r) -- check key rotation / "
                "dev->prod key transition",
                quarantined,
                session_id,
            )
        return turns

    def get_turns(self, session_id: str) -> list[Turn]:
        """Alias for get_session_turns()."""
        return self.get_session_turns(session_id)

    def add_turn(
        self,
        session_id: str,
        role: str,
        content: str,
        pgov_status: str,
        pgov_reasons: list[str],
    ) -> str:
        """Encrypt ``content`` and add a turn to a session.

        Args:
            session_id: UUID of the parent session.
            role: 'user' or 'assistant'.
            content: Plaintext text content (encrypted before storage).
            pgov_status: 'approved', 'denied', 'error', or 'N/A'.
            pgov_reasons: List of PGOV reason code strings.

        Returns:
            UUID of the new turn.

        Raises:
            ValueError: If role is not 'user' or 'assistant'.
        """
        if role not in ("user", "assistant"):
            raise ValueError(f"Invalid role: {role!r}. Must be 'user' or 'assistant'.")

        turn_id = str(uuid.uuid4())
        now = datetime.now(timezone.utc).isoformat()

        enc_content = self._enc_turn_content(turn_id, content)

        self._conn.execute(
            "INSERT INTO turns (id, session_id, role, content, pgov_status, pgov_reasons, timestamp) "
            "VALUES (?, ?, ?, ?, ?, ?, ?)",
            (turn_id, session_id, role, enc_content, pgov_status, json.dumps(pgov_reasons), now),
        )
        # Update session's updated_at
        self._conn.execute(
            "UPDATE sessions SET updated_at = ? WHERE id = ?",
            (now, session_id),
        )
        self._conn.commit()
        return turn_id

    def delete_session(self, session_id: str) -> bool:
        """Delete a session and all its turns (CASCADE).

        Args:
            session_id: UUID of the session to delete.

        Returns:
            True if the session existed and was deleted.
        """
        cursor = self._conn.execute(
            "DELETE FROM sessions WHERE id = ?",
            (session_id,),
        )
        self._conn.commit()
        deleted = cursor.rowcount > 0
        if deleted:
            logger.info(
                "EncryptedSessionStore: session deleted: %s (CASCADE turns)", session_id
            )
        return deleted

    def clear_session_turns(self, session_id: str) -> int:
        """Delete all turns for a session, preserving the session record.

        Args:
            session_id: UUID of the session.

        Returns:
            Number of turns deleted.
        """
        cursor = self._conn.execute(
            "DELETE FROM turns WHERE session_id = ?",
            (session_id,),
        )
        self._conn.commit()
        logger.info(
            "EncryptedSessionStore: cleared %d turns for session %s",
            cursor.rowcount, session_id,
        )
        return cursor.rowcount

    def set_active_session(self, session_id: str) -> None:
        """Mark a session as active and deactivate all others."""
        self._conn.execute("UPDATE sessions SET is_active = 0")
        self._conn.execute(
            "UPDATE sessions SET is_active = 1 WHERE id = ?",
            (session_id,),
        )
        self._conn.commit()

    # -- Title management --------------------------------------------------

    def set_title_if_empty(self, session_id: str, title: str) -> bool:
        """Set a session's title (encrypted) only if it is currently empty.

        In the encrypted store, "empty" means the raw title decrypts to '' (or
        is stored as '' directly).  The check is performed by reading the current
        raw title and decrypting it; if it decrypts to '' the new title is
        encrypted and written back.

        Args:
            session_id: UUID of the session.
            title: Title to set (truncated to ``SESSION_TITLE_MAX_CHARS``).

        Returns:
            True if a row was updated (title was empty and is now set).
        """
        row = self._conn.execute(
            "SELECT title FROM sessions WHERE id = ?", (session_id,)
        ).fetchone()
        if row is None:
            return False  # unknown session

        current_title = self._dec_session_title(session_id, row[0])
        if current_title != "":
            return False  # title already set -- do not clobber

        truncated = title[:SESSION_TITLE_MAX_CHARS]
        enc_title = self._enc_session_title(session_id, truncated)
        self._conn.execute(
            "UPDATE sessions SET title = ? WHERE id = ?",
            (enc_title, session_id),
        )
        self._conn.commit()
        logger.info("EncryptedSessionStore: session title set: %s", session_id)
        return True

    def update_session_title(self, session_id: str, title: str) -> bool:
        """Set a session's title unconditionally -- backs the /rename command.

        Args:
            session_id: UUID of the session.
            title: New title (truncated to ``SESSION_TITLE_MAX_CHARS``).

        Returns:
            True if the session existed and was updated.
        """
        row = self._conn.execute(
            "SELECT id FROM sessions WHERE id = ?", (session_id,)
        ).fetchone()
        if row is None:
            return False

        truncated = title[:SESSION_TITLE_MAX_CHARS]
        enc_title = self._enc_session_title(session_id, truncated)
        self._conn.execute(
            "UPDATE sessions SET title = ? WHERE id = ?",
            (enc_title, session_id),
        )
        self._conn.commit()
        logger.info("EncryptedSessionStore: session renamed: %s", session_id)
        return True

    def _backfill_empty_titles(self) -> int:
        """One-time data repair: give empty-title sessions a derived title.

        For the encrypted store, "empty" means the raw title value is '' (empty
        string) -- empty titles are never encrypted.  After backfill the derived
        title is written back encrypted.

        The first user turn's ``content`` may already be an encrypted blob if the
        store was previously opened with encryption; it is decrypted before
        deriving the title.

        Returns:
            Number of sessions backfilled.
        """
        # Only fetch sessions whose raw title column is the empty string ''.
        # Encrypted titles are stored as bytes (BLOB), not ''.  Using SQL
        # filtering here avoids calling _dec_session_title on encrypted blobs
        # for sessions that already have a title -- which matters when the store
        # is opened with a different key (e.g. during testing or key rotation).
        rows = self._conn.execute(
            "SELECT id, created_at FROM sessions WHERE title = ''"
        ).fetchall()
        backfilled = 0
        quarantined = 0
        for session_id, created_at in rows:
            turn = self._conn.execute(
                "SELECT id, content, timestamp FROM turns "
                "WHERE session_id = ? AND role = 'user' "
                "ORDER BY timestamp ASC LIMIT 1",
                (session_id,),
            ).fetchone()
            if turn is None:
                continue  # no user turn -- nothing to name the session after

            turn_id, raw_content, turn_ts = turn

            # Bulk-read quarantine policy (ADR-025 §2.7 amendment, 2026-06-06):
            # A single un-decryptable turn (legacy key, tampered ciphertext)
            # must not abort construction of the store and deny access to all
            # other sessions.  Quarantine the row: skip it, emit the stable
            # event code, and continue.  Plaintext is never returned.
            try:
                content = self._dec_turn_content(turn_id, raw_content)
            except RuntimeError as exc:
                quarantined += 1
                logger.warning(
                    "SESSION_ROW_DECRYPT_QUARANTINE event=SESSION_ROW_DECRYPT_QUARANTINE "
                    "turn_id=%r session_id=%r reason=%r -- turn excluded from "
                    "_backfill_empty_titles; session title will remain empty",
                    turn_id,
                    session_id,
                    str(exc),
                )
                continue

            when = _parse_local_datetime(created_at or turn_ts)
            title = derive_session_title(content, when)

            # Encrypt and write the derived title back.
            enc_title = self._enc_session_title(session_id, title)
            self._conn.execute(
                "UPDATE sessions SET title = ? WHERE id = ?",
                (enc_title, session_id),
            )
            backfilled += 1

        if quarantined:
            logger.warning(
                "SESSION_ROW_DECRYPT_QUARANTINE summary: %d session(s) quarantined "
                "during _backfill_empty_titles -- those sessions retain an empty title; "
                "check key rotation / dev->prod key transition",
                quarantined,
            )
        if backfilled:
            self._conn.commit()
            logger.info(
                "EncryptedSessionStore: backfilled %d empty session title(s)", backfilled
            )
        return backfilled


