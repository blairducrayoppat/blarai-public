def migrate_plaintext_to_encrypted(
    db_path: str,
    cipher: "FieldCipher",  # type: ignore[name-defined]  # noqa: F821
    *,
    dry_run: bool = False,
) -> dict[str, int]:
    """Encrypt plaintext rows in an existing sessions.db in place.

    Algorithm:
    1. Open the DB.
    2. For each turn row where ``content`` is a plaintext string:
       a. Encrypt ``content`` with AAD bound to the turn UUID.
       b. Write back the ciphertext blob.
    3. For each session row where ``title`` is a non-empty plaintext string:
       a. Encrypt ``title`` with AAD bound to the session UUID.
       b. Write back the ciphertext blob.
    4. Skip rows that are already encrypted (version-byte check -- idempotent).
    5. VACUUM to scrub freed pages containing old plaintext.

    Returns a dict with counts:
    ``{"turns_migrated": N, "titles_migrated": M,
       "turns_already_encrypted": A, "titles_already_encrypted": B,
       "errors": K}``.

    Args:
        db_path:  Path to the SQLite file.
        cipher:   :class:`~shared.security.field_cipher.FieldCipher` instance.
        dry_run:  If True, do not write anything; just report what would be done.
    """
    from shared.security.field_cipher import make_aad_for

    conn = sqlite3.connect(db_path, check_same_thread=False)
    try:
        # Ensure the sessions_meta table exists and mark encryption version --
        # skip if dry_run (no writes at all).
        if not dry_run:
            conn.executescript(_META_TABLE_SQL)
            conn.execute(
                "INSERT OR IGNORE INTO sessions_meta(key, value) VALUES('encryption_version', ?)",
                (_SESSIONS_ENCRYPTION_VERSION,),
            )
            conn.commit()

        turns_migrated = 0
        turns_already = 0
        titles_migrated = 0
        titles_already = 0
        errors = 0

        # -- Migrate turns.content -----------------------------------------
        turn_rows = conn.execute(
            "SELECT id, content FROM turns"
        ).fetchall()

        for turn_id, raw_content in turn_rows:
            if _is_encrypted_blob(raw_content):
                turns_already += 1
                continue
            try:
                plaintext = (
                    raw_content
                    if isinstance(raw_content, str)
                    else bytes(raw_content).decode("utf-8")
                )
                enc_content = cipher.encrypt(
                    plaintext.encode("utf-8"),
                    aad=make_aad_for("turns", "content", turn_id),
                )
                if not dry_run:
                    conn.execute(
                        "UPDATE turns SET content = ? WHERE id = ?",
                        (enc_content, turn_id),
                    )
                turns_migrated += 1
            except Exception as exc:  # noqa: BLE001
                logger.error("Migration error on turn %s: %s", turn_id, exc)
                errors += 1

        # -- Migrate sessions.title ----------------------------------------
        session_rows = conn.execute(
            "SELECT id, title FROM sessions"
        ).fetchall()

        for session_id, raw_title in session_rows:
            # Empty titles stay as '' -- never encrypt an empty title.
            if raw_title is None or raw_title == "" or raw_title == b"":
                continue
            if _is_encrypted_blob(raw_title):
                titles_already += 1
                continue
            try:
                plaintext = (
                    raw_title
                    if isinstance(raw_title, str)
                    else bytes(raw_title).decode("utf-8")
                )
                enc_title = cipher.encrypt(
                    plaintext.encode("utf-8"),
                    aad=make_aad_for("sessions", "title", session_id),
                )
                if not dry_run:
                    conn.execute(
                        "UPDATE sessions SET title = ? WHERE id = ?",
                        (enc_title, session_id),
                    )
                titles_migrated += 1
            except Exception as exc:  # noqa: BLE001
                logger.error("Migration error on session %s title: %s", session_id, exc)
                errors += 1

        if not dry_run:
            conn.commit()
            # VACUUM to scrub freed pages that still contain old plaintext.
            conn.execute("VACUUM")

        return {
            "turns_migrated": turns_migrated,
            "titles_migrated": titles_migrated,
            "turns_already_encrypted": turns_already,
            "titles_already_encrypted": titles_already,
            "errors": errors,
        }
    finally:
        conn.close()


def verify_no_plaintext(
    db_path: str,
    plaintext_samples: list[bytes],
) -> list[str]:
    """Whole-file scan asserting no plaintext sample appears in raw DB bytes.

    Reads the raw file bytes (post-VACUUM) and searches for each supplied
    sample. Returns a list of violation strings (empty = all clear).

    Args:
        db_path:           Path to the SQLite file.
        plaintext_samples: List of plaintext byte strings to search for.
    """
    from pathlib import Path

    raw = Path(db_path).read_bytes()
    violations: list[str] = []
    for sample in plaintext_samples:
        if len(sample) < 4:
            continue  # too short to be meaningful
        if sample in raw:
            violations.append(
                f"Plaintext sample found in raw DB file: {sample[:40]!r}..."
            )
    return violations


