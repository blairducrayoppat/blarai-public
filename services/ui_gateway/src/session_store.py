class StoreProvisioningError(RuntimeError):
    """Raised by the store factories when a required keystore is absent in
    production mode (``dev_mode=False``).

    This is the symmetric counterpart to ``AuditProvisioningError`` in the
    Policy Agent (ADR-025 §2.8(a)).  A production boot with
    ``BLARAI_DEK_KEYSTORE`` unset is a misconfiguration -- the store refuses
    to start rather than silently falling back to the public ``SoftwareSealer``
    key (which is not a security boundary).  Fail-closed: no plaintext
    fallback, no SoftwareSealer in production.
    """


def build_session_store(
    db_path: str,
    *,
    dev_mode: bool = False,
) -> "EncryptedSessionStore":
    """Build an :class:`EncryptedSessionStore` -- the production factory.

    Follows the same DEK-envelope construction pattern as ``_build_substrate``
    in the Orchestrator entrypoint (EA-3 reference pattern, ADR-025):

    - Dev/test path (``dev_mode=True`` **explicit**, or ``db_path == ':memory:'``):
      ``SoftwareSealer`` + ephemeral keystore alongside the DB.  The dev path
      is an EXPLICIT, LOUD opt-in -- it is NOT the silent default for a missing
      env var.
    - Production path (``dev_mode=False``, ``BLARAI_DEK_KEYSTORE`` set):
      ``TpmSealer("BlarAI-DEKSeal")`` + the persisted keystore.
    - Misconfiguration (``dev_mode=False``, ``BLARAI_DEK_KEYSTORE`` MISSING,
      non-``:memory:`` db_path): REFUSE TO START -- raises
      :class:`StoreProvisioningError`.  Symmetric with the audit
      refuse-to-start (ADR-025 §2.8(a)).

    The session store and the substrate store share the **same DEK** (same
    ``BLARAI_DEK_KEYSTORE`` keystore path, same ``TpmSealer`` key name
    ``"BlarAI-DEKSeal"``).  If both stores are constructed in the same process
    they load the same keystore file, and the ``TpmSealer`` unseals the same DEK
    -- one DEK, two consumers (ADR-025 section 2.1).  The envelope is loaded (not
    re-created) if the keystore already exists, so re-opening an existing DB
    reuses the persisted DEK.

    Args:
        db_path:  Path to the sessions SQLite file.
        dev_mode: Caller-supplied dev-mode flag (from launcher / entrypoint).
                  ``True`` permits the ``SoftwareSealer`` path and is intended
                  only for development and test.  Defaults to ``False``
                  (production posture -- refuse if keystore is absent).

    Returns:
        A fully-initialized :class:`EncryptedSessionStore`.

    Raises:
        StoreProvisioningError: if ``dev_mode=False`` and
            ``BLARAI_DEK_KEYSTORE`` is not set (and ``db_path != ':memory:'``).
            Production refuses to construct with a SoftwareSealer.
        DekEnvelopeError:   if the DEK cannot be unsealed (fail-closed).
        DevModeSealerError: if a SoftwareSealer is used without dev_mode=True.
    """
    from pathlib import Path

    from shared.security.dek_envelope import (
        DekEnvelope,
        build_envelope,
        generate_recovery_key,
    )
    from shared.security.field_cipher import FieldCipher, derive_subkeys
    from shared.security.tpm_sealer import SoftwareSealer

    keystore_env = os.environ.get("BLARAI_DEK_KEYSTORE", "")

    if db_path == ":memory:":
        # Pure in-memory -- always dev/test; envelope is ephemeral, not saved.
        sealer = SoftwareSealer()
        recovery_key = generate_recovery_key()
        envelope = DekEnvelope.create(sealer=sealer, recovery_key=recovery_key)
    elif dev_mode:
        # Explicit dev/test path -- SoftwareSealer with ephemeral keystore
        # alongside the DB.  LOUD warning so this is never silent.
        sealer = SoftwareSealer()
        recovery_key = generate_recovery_key()
        keystore_path = Path(db_path).with_suffix(".keystore.json")
        if keystore_path.exists():
            envelope = DekEnvelope.load(sealer=sealer, keystore_path=keystore_path)
        else:
            envelope = build_envelope(
                sealer=sealer,
                recovery_key=recovery_key,
                keystore_path=keystore_path,
                dev_mode=True,
            )
            logger.warning(
                "SessionStore: created ephemeral DEK keystore at %s "
                "(dev mode -- SoftwareSealer, no TPM). "
                "Run the ceremony to provision a real TPM-sealed DEK.",
                keystore_path,
            )
    elif not keystore_env:
        # Production path with missing keystore -- REFUSE TO START.
        # Symmetric with the audit refuse-to-start (ADR-025 §2.8(a)).
        # A missing BLARAI_DEK_KEYSTORE in production is a misconfiguration;
        # silently falling back to SoftwareSealer would encrypt under a
        # hard-coded public key, providing NO security guarantee.
        logger.error(
            "BLARAI_DEK_KEYSTORE is not set and dev_mode=False -- refusing to "
            "start.  Production requires a TPM-sealed DEK keystore.  Run the "
            "provisioning ceremony or set dev_mode=True for development use "
            "(ADR-025 §2.8(a))."
        )
        raise StoreProvisioningError(
            "BLARAI_DEK_KEYSTORE is not set in production mode (dev_mode=False).  "
            "Production requires a TPM-sealed DEK keystore; constructing a session "
            "store with the SoftwareSealer is NOT permitted outside an explicit "
            "dev_mode=True context.  Run the provisioning ceremony or start the "
            "launcher in development mode (ADR-025 §2.8(a))."
        )
    else:
        # Production path -- TpmSealer.
        from shared.security.tpm_sealer import TpmSealer
        sealer = TpmSealer(key_name="BlarAI-DEKSeal")
        envelope = DekEnvelope.load(sealer=sealer, keystore_path=Path(keystore_env))

    dek = envelope.unseal_dek()
    subkeys = derive_subkeys(dek)
    cipher = FieldCipher(subkeys)

    store = EncryptedSessionStore(db_path=db_path, cipher=cipher)
    # Production-wiring regression lock: has_encryption MUST be True.
    assert store.has_encryption is True, (
        "REGRESSION: EncryptedSessionStore.has_encryption is not True -- "
        "encryption wiring silently disabled"
    )
    return store
