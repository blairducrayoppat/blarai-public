logger = logging.getLogger(__name__)

#: ``turns.pgov_status`` marker for INFORMATIONAL assistant turns (#655) --
#: deterministic gateway/tool output echoed into the transcript (ingest
#: previews, the bare-URL nudge, ingest decisions).  These turns are NOT model
#: output and were never PGOV-validated, so none of the model-turn statuses
#: ('approved'/'denied'/'error') is honest for them.  The column has no CHECK
#: constraint, so the new value is a pure data convention.  Two load-bearing
#: consequences, both deliberate:
#:   * The prompt-history filter (transport.send_prompt) forwards only
#:     'approved' assistant turns, so informational turns (which can carry an
#:     article-sized preview) never re-enter the model's context or the 40 KB
#:     history budget.
#:   * The WinUI renders by ``pgov_status == 'denied'`` only (ChatModels.cs
#:     ``IsDenied``), so informational turns reload as normal assistant
#:     messages with no UI change.
INFORMATIONAL_TURN_STATUS: str = "informational"


def _harden_db_file_dacl(db_path: str) -> None:
    """Apply the #637 owner-only DACL to the session DB file (defense-in-depth).

    DATA_MAP §7 item 1: lock ``sessions.db`` to (current user + SYSTEM) full
    control on top of the at-rest encryption.  Owner-preserving + fail-safe
    (``shared.security.file_dacl.ensure_owner_only_dacl`` never raises and never
    blocks access).  A no-op for ``:memory:`` stores and on non-Windows hosts.
    Called after ``sqlite3.connect`` so the DB file exists on disk.
    """
    if db_path == ":memory:":
        return
    from shared.security.file_dacl import ensure_owner_only_dacl

    ensure_owner_only_dacl(db_path)


# ---------------------------------------------------------------------------
# Title derivation
# ---------------------------------------------------------------------------


def _parse_local_datetime(iso_ts: str) -> datetime:
    """Parse a stored ISO-8601 UTC timestamp into a local-time datetime.

    Stored timestamps are UTC (``datetime.now(timezone.utc).isoformat()``);
    titles show a human-facing date, so the date is rendered in local time.
    Falls back to the current local time if the string cannot be parsed, so
    a single malformed historical row never breaks the title backfill.
    """
    try:
        dt = datetime.fromisoformat(iso_ts)
    except (ValueError, TypeError):
        return datetime.now()
    if dt.tzinfo is not None:
        dt = dt.astimezone()  # UTC -> local
    return dt


def derive_session_title(prompt_text: str, when: datetime) -> str:
    """Build an auto-generated session title from the first user prompt.

    Format: ``<first N chars of prompt>… \xb7 <Mon D, YYYY>`` -- for example
    ``What's the capi… \xb7 May 22, 2026``. The prompt fragment makes the
    session distinguishable by content; the date disambiguates sessions
    that open with similar prompts. ``N`` is ``SESSION_TITLE_PROMPT_CHARS``.

    Args:
        prompt_text: The user's first prompt for the session.
        when: Datetime to stamp the title with (local time expected -- the
            caller passes the first-prompt / session-creation moment).

    Returns:
        A non-empty title string, capped at ``SESSION_TITLE_MAX_CHARS``.
    """
    collapsed = " ".join(prompt_text.split())
    fragment = collapsed[:SESSION_TITLE_PROMPT_CHARS]
    if len(collapsed) > SESSION_TITLE_PROMPT_CHARS:
        fragment += "…"  # ellipsis -- signals the prompt was truncated
    date_part = f"{when:%b} {when.day}, {when.year}"
    title = f"{fragment} \xb7 {date_part}" if fragment else f"Session \xb7 {date_part}"
    return title[:SESSION_TITLE_MAX_CHARS]


