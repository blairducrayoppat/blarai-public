def test_list_generated_images_metadata_only(bank: EncryptedKnowledgeBank) -> None:
    """list_generated_images returns cheap METADATA (no prompt, no bytes) and the
    saved flag — newest first.  The NamedTuple has no prompt/data field at all."""
    a, b_id = uuid.uuid4().hex, uuid.uuid4().hex
    bank.store_generated_image(
        image_id=a, session_id="s1", image_bytes=_PNG, mime="image/png",
        prompt="SECRET-PROMPT-A",
    )
    bank.store_generated_image(
        image_id=b_id, session_id="s2", image_bytes=_JPEG, mime="image/jpeg",
        prompt="SECRET-PROMPT-B",
    )
    metas = bank.list_generated_images()
    assert len(metas) == 2
    assert all(isinstance(m, GeneratedImageMeta) for m in metas)
    # METADATA ONLY: the record type carries no prompt and no bytes field.
    assert not hasattr(metas[0], "prompt")
    assert not hasattr(metas[0], "data")
    # Newest first (b stored after a).
    ids = [m.image_id for m in metas]
    assert ids == [b_id, a]
    by_id = {m.image_id: m for m in metas}
    assert by_id[a].session_id == "s1"
    assert by_id[a].mime == "image/png"
    assert by_id[a].saved is False  # never saved yet
    assert by_id[a].byte_size > 0  # length(data) ciphertext size, not a decrypt
    assert by_id[b_id].mime == "image/jpeg"


def test_list_generated_images_does_not_decrypt(
    bank: EncryptedKnowledgeBank, monkeypatch
) -> None:
    """Building the listing must NEVER call the field cipher's decrypt — the list
    is metadata-only by construction.  Patch the bank's decrypt to explode and
    assert list still works (a decrypt would blow up the test)."""
    iid = uuid.uuid4().hex
    bank.store_generated_image(
        image_id=iid, session_id="s1", image_bytes=_PNG, mime="image/png",
        prompt="SECRET",
    )

    def _boom(*_a, **_k):  # pragma: no cover — must never be reached by list
        raise AssertionError("list_generated_images must not decrypt anything")

    monkeypatch.setattr(bank._cipher, "decrypt", _boom)
    metas = bank.list_generated_images()
    assert len(metas) == 1
    assert metas[0].image_id == iid


