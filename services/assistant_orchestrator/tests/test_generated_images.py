def test_saved_migration_adds_column_to_pre_existing_table(tmp_path: Path) -> None:
    """A store created WITHOUT the saved column (an older build) gains it on the
    next open via the idempotent ALTER-TABLE migration; existing rows migrate to
    not-saved (past /saves are not retroactively known)."""
    import sqlite3

    db = str(tmp_path / "legacy_knowledge.db")
    cipher = _make_cipher()

    # 1. Build a store, then SIMULATE the pre-#667 schema by dropping the saved
    #    column (SQLite has no DROP COLUMN on old versions, so rebuild the table
    #    in the OLD shape with a row in it, then reopen via EncryptedKnowledgeBank).
    b = _make_bank(db_path=db, cipher=cipher)
    iid = uuid.uuid4().hex
    b.store_generated_image(
        image_id=iid, session_id="s1", image_bytes=_PNG, mime="image/png", prompt="x",
    )
    b.close()

    # Recreate generated_images WITHOUT the saved column, preserving the row.
    raw = sqlite3.connect(db)
    cols = [r[1] for r in raw.execute("PRAGMA table_info(generated_images)").fetchall()]
    assert "saved" in cols, "fresh build should already have the column"
    raw.executescript(
        """
        ALTER TABLE generated_images RENAME TO _gi_new;
        CREATE TABLE generated_images (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            image_id TEXT NOT NULL UNIQUE,
            session_id TEXT NOT NULL,
            image_hash BLOB NOT NULL,
            mime TEXT NOT NULL,
            prompt BLOB NOT NULL,
            data BLOB NOT NULL,
            created_at TEXT NOT NULL
        );
        INSERT INTO generated_images
            (id, image_id, session_id, image_hash, mime, prompt, data, created_at)
        SELECT id, image_id, session_id, image_hash, mime, prompt, data, created_at
        FROM _gi_new;
        DROP TABLE _gi_new;
        """
    )
    raw.commit()
    cols_legacy = [
        r[1] for r in raw.execute("PRAGMA table_info(generated_images)").fetchall()
    ]
    raw.close()
    assert "saved" not in cols_legacy, "legacy table must lack the column"

    # 2. Reopen via EncryptedKnowledgeBank — the migration adds the column.
    b2 = _make_bank(db_path=db, cipher=cipher)
    try:
        cols_after = [
            r[1]
            for r in b2._conn.execute(
                "PRAGMA table_info(generated_images)"
            ).fetchall()
        ]
        assert "saved" in cols_after, "migration must add the saved column"
        # The pre-existing row survived and reads as not-saved.
        metas = b2.list_generated_images()
        assert len(metas) == 1
        assert metas[0].image_id == iid
        assert metas[0].saved is False
        # And the row still decrypts (migration touched no encrypted column).
        got = b2.get_generated_image(iid)
        assert got is not None and got.data == _PNG
        # Idempotent: a SECOND open does not error or duplicate the column.
        b2.close()
        b3 = _make_bank(db_path=db, cipher=cipher)
        b3.close()
    finally:
        try:
            b2.close()
        except Exception:
            pass
