"""
Tests for UC-010 locally-generated images in the Encrypted Knowledge Bank
(ADR-033 — DORMANT build).

Covers the Module C storage contract for ``generated_images``:
  * ``store_generated_image`` / ``get_generated_image`` round-trip.
  * at-rest encryption: prompt + data are ciphertext on disk; mime is a
    plaintext label.
  * AAD binding: a row re-associated to a DIFFERENT session must refuse to
    decrypt (quarantined → None), never partial plaintext.
  * DELETE-on-discard: ``delete_generated_image`` reaps the row outright +
    is idempotent.
  * keyed-hash dedup index over bytes (same bytes → same index).
  * THE NO-VLM STRUCTURAL LOCK: generated bytes never reach the embedder.

Reuses the deterministic stub-embedder fixtures from ``test_knowledge_bank`` so
no ONNX model is required (worktree-safe).
"""

