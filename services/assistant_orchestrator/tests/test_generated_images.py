def test_session_aad_binding_quarantines(bank: EncryptedKnowledgeBank) -> None:
    """A row whose plaintext session_id is rewritten must refuse to decrypt under
    the tampered session — quarantined to None (AAD binds session_id|image_id)."""
    from shared.security.field_cipher import FieldCipherError

    iid = uuid.uuid4().hex
    bank.store_generated_image(
        image_id=iid, session_id="real-session", image_bytes=_PNG,
        mime="image/png", prompt="x",
    )
    # Tamper: rewrite the plaintext session_id column to a different session.
    with bank._conn:
        bank._conn.execute(
            "UPDATE generated_images SET session_id=? WHERE image_id=?",
            ("attacker-session", iid),
        )
    # The direct field decrypt under the tampered session must refuse.
    raw = bank._conn.execute(
        "SELECT data FROM generated_images WHERE image_id=?", (iid,)
    ).fetchone()[0]
    with pytest.raises(FieldCipherError):
        bank._dec_generated_field("data", "attacker-session", iid, raw)
    # And the read-by-id quarantines (returns None — never partial plaintext).
    assert bank.get_generated_image(iid) is None


def test_wrong_image_id_aad_refuses(bank: EncryptedKnowledgeBank) -> None:
    from shared.security.field_cipher import FieldCipherError

    a, b_id = uuid.uuid4().hex, uuid.uuid4().hex
    bank.store_generated_image(
        image_id=a, session_id="s1", image_bytes=_PNG, mime="image/png", prompt="a",
    )
    raw_a = bank._conn.execute(
        "SELECT data FROM generated_images WHERE image_id=?", (a,)
    ).fetchone()[0]
    # a's blob under b's image_id must fail authentication.
    with pytest.raises(FieldCipherError):
        bank._dec_generated_field("data", "s1", b_id, raw_a)


