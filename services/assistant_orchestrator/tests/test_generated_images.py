def test_same_bytes_same_hash(bank: EncryptedKnowledgeBank) -> None:
    a, b_id = uuid.uuid4().hex, uuid.uuid4().hex
    bank.store_generated_image(
        image_id=a, session_id="s1", image_bytes=_PNG, mime="image/png", prompt="a",
    )
    bank.store_generated_image(
        image_id=b_id, session_id="s2", image_bytes=_PNG, mime="image/png", prompt="b",
    )
    ha = bank._conn.execute(
        "SELECT image_hash FROM generated_images WHERE image_id=?", (a,)
    ).fetchone()[0]
    hb = bank._conn.execute(
        "SELECT image_hash FROM generated_images WHERE image_id=?", (b_id,)
    ).fetchone()[0]
    assert bytes(ha) == bytes(hb)  # identical bytes → identical keyed index


