"""Local image generation via OpenVINO GenAI diffusion pipelines (UC-010, ADR-033).

Two modes, both fully local on the Arc 140V iGPU, **no external network**:
  * ``generate_text2image(prompt, ...)``     — text → image (Text2ImagePipeline)
  * ``generate_image2image(image, prompt)``  — image + text → image (Image2ImagePipeline)

Structural clone of :mod:`shared.inference.vlm` — the proven load-on-demand +
**Fail-Soft** + ``unload()`` + ``log_memory`` template. Any failure (model
missing, disabled, GPU OOM, ``openvino_genai`` absent, manifest mismatch, decode
error) returns ``None`` so callers degrade to a clear "generation unavailable"
notice rather than crashing the app.

**Dormant by default (ADR-033).** The capability ships ``enabled=false`` AND the
uncensored SDXL model is gitignored, so :func:`is_available` is False on a clean
checkout and nothing ever loads. Activation is a SEPARATE LA-present go-live
ceremony (operator content attestation → flip ``[image_generation].enabled`` →
live GPU verify), never a runtime decision.

**Memory posture (ADR-033 §memory).** The diffusion model (~4.4 GB INT8) is too
large to co-reside with the always-resident 14B; it loads on demand and is
evicted after every generate (``unload()`` in the caller's ``finally``). GPU
props deliberately OMIT ``MODEL_PRIORITY="HIGH"`` (the 14B keeps priority; the
diffusion model yields). The 14B is NEVER evicted — the AO orchestrates the
*other* resident caches (VLM + embedder + voice) down before a generate
(``entrypoint`` §E). Phase-0 spike (2026-06-16, Arc 140V): 14B core + ~3k KV +
SDXL INT8 + a 1024² generate peaked ~26.0 GB vs the 31.323 GB ceiling (5.3 GB
headroom); SDXL load 18.7 s, 1024² generate 10.7 s.

**Weight integrity (fail-closed).** ``_get_pipe`` verifies every nested
``.bin`` weight against the SHA-256 manifest before construction
(``weight_integrity.verify_all_manifest_entries_nested``); a mismatch refuses
the load. The runtime NEVER fetches weights.

**Output provenance (ADR-033).** Generated bytes are born on-box from an
operator prompt, are display-only, and are NEVER chunked, embedded, indexed, or
fed to any model. This module only produces the PNG bytes; the AO stores them
born-encrypted (``knowledge_bank.store_generated_image``).
"""

from __future__ import annotations

