"""Production manifest stager — computes real SHA-256 digests + writes manifest.

Run ONCE after downloading the Qwen3-14B OpenVINO model files to stage the
Known-Good Manifest at the configured path.  The manifest is a per-machine
artifact tied to the actual binary files on disk, so it is git-ignored and
regenerated by this command on the deployment host.

Usage::

    python -m shared.models.stage_production_manifest

The command:
  1. Locates the model directory (``models/qwen3-14b/openvino-int4-gpu`` by
     default, matching ``services/.../config/default.toml``).
  2. Computes SHA-256 for every ``*.bin`` file in that directory (reusing
     ``shared.models.weight_integrity.compute_sha256``).
  3. Writes the manifest JSON to the configured path
     (``models/qwen3-14b/openvino-int4-gpu/manifest.json`` by default).

Idempotent: running it again re-hashes and re-writes the manifest.  Safe to
re-run after a model update.

Fail-Closed:
  - If the model directory does not exist → clear error, no partial write.
  - If no ``*.bin`` files are found → clear error, no partial write.
  - If any individual file hash fails → clear error, no partial write.
  - The manifest is written atomically (written to a ``.tmp`` file, then
    renamed so a crash during write does not leave a truncated manifest).

Does NOT sign the manifest.  Signing (FUT-04 / ``require_signed_manifest``)
is out of scope for this helper; set ``require_signed_manifest = false``
(the default) during the ceremony and beyond until the signing ceremony runs.
"""

from __future__ import annotations

import json
import os
import sys
from pathlib import Path
from typing import Final

from shared.models.weight_integrity import compute_sha256

# ---------------------------------------------------------------------------
# Default paths — must match services/.../config/default.toml
# ---------------------------------------------------------------------------

# The model directory and manifest path are hard-coded to the well-known
# production location so the LA does not need to supply any arguments.
# The BLARAI_MODEL_DIR env var provides an override for testing.

_DEFAULT_MODEL_DIR: Final[str] = "models/qwen3-14b/openvino-int4-gpu"
_MANIFEST_FILENAME: Final[str] = "manifest.json"
_MANIFEST_VERSION: Final[str] = "1.0.0"

# FUT-05 / #107 + #917 — the ENFORCED speculative-decoding DRAFT model dirs (flat
# layout, top-dir *.bin): both served drafts, each verified by a real code path and
# signed by the ceremony. ``--drafts`` re-stages them so the signing ceremony has a
# single documented staging command.
#   - qwen3-0.6b-pruned-6l/openvino-int8-gpu — shared-pipeline draft (#107).
#   - qwen3-0.6b/openvino-int4-gpu — PA standalone/fallback draft (#917).
# Deliberately excluded: qwen2.5-1.5b (not a served model at all).
_DRAFT_MODEL_DIRS: Final[tuple[str, ...]] = (
    "models/qwen3-0.6b-pruned-6l/openvino-int8-gpu",
    "models/qwen3-0.6b/openvino-int4-gpu",
)


def _model_dir() -> Path:
    override = os.environ.get("BLARAI_MODEL_DIR", "")
    if override:
        return Path(override)
    return Path(_DEFAULT_MODEL_DIR)


def _manifest_path(model_dir: Path) -> Path:
    override = os.environ.get("BLARAI_MANIFEST_PATH", "")
    if override:
        return Path(override)
    return model_dir / _MANIFEST_FILENAME


# ---------------------------------------------------------------------------
# Visual helpers
# ---------------------------------------------------------------------------


def _header(title: str) -> None:
    width = 70
    print()
    print("=" * width)
    print(f"  {title}")
    print("=" * width)
    print()


def _section(label: str) -> None:
    print(f"\n--- {label} ---")


# ---------------------------------------------------------------------------
# Core logic
# ---------------------------------------------------------------------------


def stage_manifest(
    model_dir: Path,
    manifest_path: Path,
) -> int:
    """Hash every .bin in ``model_dir`` and write the manifest to ``manifest_path``.

    Returns 0 on success, 1 on any failure (Fail-Closed — no partial write).
    """
    _header("BlarAI Production Manifest Stager")

    # ── Step 1: Validate model directory ────────────────────────────────────
    _section("Step 1 of 3 — Locate model files")
    print(f"  Model directory : {model_dir}")

    if not model_dir.exists():
        print(
            f"\nFAIL-CLOSED: model directory not found:\n  {model_dir}\n\n"
            "Check that the Qwen3-14B model has been downloaded to the\n"
            "correct location, then re-run this command.\n"
            "Expected layout:\n"
            f"  {model_dir}/\n"
            "    openvino_model.bin\n"
            "    openvino_tokenizer.bin\n"
            "    openvino_detokenizer.bin\n"
            "    (other model files)\n",
            file=sys.stderr,
        )
        return 1

    bin_files = sorted(model_dir.glob("*.bin"))
    if not bin_files:
        print(
            f"\nFAIL-CLOSED: no .bin files found in:\n  {model_dir}\n\n"
            "The model files have not been downloaded or are in a different\n"
            "directory.  Download the Qwen3-14B OpenVINO INT4 model first.\n",
            file=sys.stderr,
        )
        return 1

    print(f"  Found {len(bin_files)} .bin file(s) to hash:")
    for f in bin_files:
        size_mb = f.stat().st_size / (1024 * 1024)
        print(f"    {f.name}  ({size_mb:.1f} MB)")

    # ── Step 2: Compute SHA-256 for each file ─────────────────────────────
    _section("Step 2 of 3 — Compute SHA-256 digests")
    print("  This may take a moment for large model files...")
    print()

    digests: dict[str, str] = {}
    for bin_file in bin_files:
        try:
            digest = compute_sha256(bin_file)
        except OSError as exc:
            print(
                f"\nFAIL-CLOSED: could not read '{bin_file.name}': {exc}\n"
                "No manifest was written.  Check the file permissions and retry.\n",
                file=sys.stderr,
            )
            return 1
        digests[bin_file.name] = digest
        print(f"  {bin_file.name}")
        print(f"    SHA-256: {digest}")

    # ── Step 3: Write manifest atomically ─────────────────────────────────
    _section("Step 3 of 3 — Write manifest")

    manifest_data: dict = {
        "version": _MANIFEST_VERSION,
        "digests": digests,
    }
    manifest_json = json.dumps(manifest_data, indent=2) + "\n"

    # Atomic write: write to a .tmp file, then rename.
    tmp_path = manifest_path.with_suffix(".json.tmp")
    try:
        manifest_path.parent.mkdir(parents=True, exist_ok=True)
        tmp_path.write_text(manifest_json, encoding="utf-8")
        # Use replace() not rename(): on Windows, rename() raises WinError 183
        # if the target already exists; replace() atomically overwrites it.
        tmp_path.replace(manifest_path)
    except OSError as exc:
        # Clean up the .tmp file if it exists.
        try:
            tmp_path.unlink(missing_ok=True)
        except OSError:
            pass
        print(
            f"\nFAIL-CLOSED: could not write manifest to:\n  {manifest_path}\n"
            f"Error: {exc}\n",
            file=sys.stderr,
        )
        return 1

    print(f"  Manifest written : {manifest_path}")
    print(f"  Entries          : {len(digests)} file(s)")

    # ── Summary ─────────────────────────────────────────────────────────────
    _header("Manifest Staged Successfully")
    print(f"  Manifest path  : {manifest_path}")
    print(f"  Digests staged : {len(digests)} .bin file(s)")
    print()
    print("  The production manifest is now ready.  Run the ceremony preflight")
    print("  to confirm it passes the digest check:")
    print()
    print("    python -m shared.security.ceremony_preflight")
    print()
    print("  NOTE: This manifest is per-machine (gitignored).  If you ever")
    print("  update or re-download the model files, re-run this command.")
    print()
    return 0


