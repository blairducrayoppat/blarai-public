def stage_manifest_nested(
    model_dir: Path,
    manifest_path: Path,
) -> int:
    """Hash a NESTED (diffusers-OV / SDXL) layout + write a relative-path manifest.

    For UC-010 (ADR-033 / WS1): walks ``model_dir`` recursively and hashes every
    ``.bin`` weight, every OpenVINO ``.xml`` topology file, AND ``model_index.json``,
    keyed by the relative POSIX path (``unet/openvino_model.bin``,
    ``unet/openvino_model.xml``, ``model_index.json``, …). The produced manifest is
    exactly what :func:`shared.models.weight_integrity.verify_all_manifest_entries_nested`
    requires (so a tampered/extra ``.xml`` or ``model_index.json`` is rejected at
    load). Mirrors the flat :func:`stage_manifest` contract (atomic write,
    fail-closed, returns 0/1) but walks subdirs. The flat 14B stager is unaffected.

    Does NOT sign — signing is a SEPARATE provisioning-ceremony step
    (``shared.models.manifest_signer.sign_manifest``), run after this on the box.
    """
    _header("BlarAI Production Manifest Stager (nested / SDXL)")

    _section("Step 1 of 3 — Locate nested model files")
    print(f"  Model directory : {model_dir}")
    if not model_dir.exists():
        print(
            f"\nFAIL-CLOSED: model directory not found:\n  {model_dir}\n\n"
            "Check that the SDXL model has been downloaded to the correct\n"
            "location (the diffusers-OV INT8 layout: unet/ vae_*/ text_encoder*/\n"
            "model_index.json), then re-run this command.\n",
            file=sys.stderr,
        )
        return 1

    swept: set[Path] = set()
    swept.update(model_dir.rglob("*.bin"))
    swept.update(model_dir.rglob("*.xml"))
    swept.update(model_dir.rglob("model_index.json"))
    targets = sorted(swept)
    if not targets:
        print(
            f"\nFAIL-CLOSED: no .bin/.xml/model_index.json found in:\n  {model_dir}\n\n"
            "The model files have not been downloaded or are in a different\n"
            "directory.\n",
            file=sys.stderr,
        )
        return 1

    print(f"  Found {len(targets)} file(s) to hash (.bin + .xml + model_index.json).")

    _section("Step 2 of 3 — Compute SHA-256 digests")
    print("  This may take a moment for large model files...")
    print()
    digests: dict[str, str] = {}
    for f in targets:
        rel = f.relative_to(model_dir).as_posix()
        try:
            digest = compute_sha256(f)
        except OSError as exc:
            print(
                f"\nFAIL-CLOSED: could not read '{rel}': {exc}\n"
                "No manifest was written.\n",
                file=sys.stderr,
            )
            return 1
        digests[rel] = digest
        print(f"  {rel}")
        print(f"    SHA-256: {digest}")

    _section("Step 3 of 3 — Write manifest")
    manifest_data: dict = {"version": _MANIFEST_VERSION, "digests": digests}
    manifest_json = json.dumps(manifest_data, indent=2) + "\n"
    tmp_path = manifest_path.with_suffix(".json.tmp")
    try:
        manifest_path.parent.mkdir(parents=True, exist_ok=True)
        tmp_path.write_text(manifest_json, encoding="utf-8")
        tmp_path.replace(manifest_path)
    except OSError as exc:
        try:
            tmp_path.unlink(missing_ok=True)
        except OSError:
            pass
        print(
            f"\nFAIL-CLOSED: could not write manifest to:\n  {manifest_path}\n"
            f"Error: {exc}\n",
            file=sys.stderr,
        )
        return 1

    print(f"  Manifest written : {manifest_path}")
    print(f"  Entries          : {len(digests)} file(s)")

    _header("Nested Manifest Staged Successfully")
    print(f"  Manifest path  : {manifest_path}")
    print(f"  Digests staged : {len(digests)} file(s) (.bin + .xml + model_index.json)")
    print()
    print("  NEXT (go-live): SIGN this manifest before flipping enabled, with")
    print("  shared.models.manifest_signer.sign_manifest(<this manifest.json>)")
    print("  (writes the .sig + .pub). [image_generation].require_signed_manifest")
    print("  then requires the valid .sig at load (ADR-033 / FUT-04).")
    print()
    print("  NOTE: this manifest is per-machine (gitignored). Re-run after any")
    print("  model update, then re-sign.")
    print()
    return 0


def stage_drafts() -> int:
    """Stage the enforced speculative-decoding DRAFT manifest (FUT-05 / #107).

    Runs the flat :func:`stage_manifest` for each dir in ``_DRAFT_MODEL_DIRS`` that
    is present (the shared-pipeline draft; flat top-dir-``*.bin`` layout). A draft
    that is absent (gitignored / not on this box) is skipped with a note, NOT a
    failure. Returns 0 when every present draft staged cleanly, 1 if any present
    draft failed to stage (fail-closed).
    """
    _header("BlarAI Draft Manifest Stager (FUT-05 / #107)")
    staged = 0
    skipped = 0
    failed = 0
    for rel in _DRAFT_MODEL_DIRS:
        model_dir = Path(rel)
        if not model_dir.exists():
            print(f"  [skip] absent (gitignored / not on this box): {model_dir}")
            skipped += 1
            continue
        manifest_path = model_dir / _MANIFEST_FILENAME
        rc = stage_manifest(model_dir, manifest_path)
        if rc == 0:
            staged += 1
        else:
            failed += 1
    print()
    print(f"  Drafts staged  : {staged}")
    print(f"  Drafts skipped : {skipped} (absent)")
    print(f"  Drafts failed  : {failed}")
    print()
    print("  NEXT: sign the 14B + this draft with the SAME key —")
    print("    python -m shared.security.provision_manifest_signing_key")
    print()
    return 1 if failed else 0


def main(argv: list[str] | None = None) -> int:
    """CLI entrypoint.

    ``--nested`` (or ``BLARAI_MANIFEST_NESTED=1``) stages the diffusers-OV
    (SDXL / UC-010) NESTED layout — hashing nested ``.bin`` + ``.xml`` +
    ``model_index.json`` keyed by relative path. ``--drafts`` stages the enforced
    speculative-decoding DRAFT manifest (shared-pipeline draft; FUT-05 / #107). The
    default is the flat 14B layout (top-dir ``*.bin`` only), unchanged.
    """
    args = list(argv if argv is not None else sys.argv[1:])
    if "--drafts" in args:
        return stage_drafts()
    nested = "--nested" in args or os.environ.get(
        "BLARAI_MANIFEST_NESTED", ""
    ).strip().lower() in ("1", "true", "yes")
    model_dir = _model_dir()
    manifest_path = _manifest_path(model_dir)
    if nested:
        return stage_manifest_nested(model_dir, manifest_path)
    return stage_manifest(model_dir, manifest_path)


if __name__ == "__main__":
    raise SystemExit(main())
